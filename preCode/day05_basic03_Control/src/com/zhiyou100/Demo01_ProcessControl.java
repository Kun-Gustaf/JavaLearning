package com.zhiyou100;

public class Demo01_ProcessControl {

    public static void main(String[] args) {
        /*
             流程控制：
                  三种结构：顺序结构 选择结构 循环结构

         */
        //顺序结构：默认的就是顺序结构,它是我们编写程序首要考虑的结构
        //从上往下依次(逐行)加载，直到程序结束
        int a = 10;
        System.out.println(a);//10
        a ++;
        System.out.println(a);//11
        --a;
        System.out.println(a+"🐖");//10


        //选择结构


    }
}
